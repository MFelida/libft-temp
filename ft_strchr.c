#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	c = c & 0xff;
	while (*s)
	{
		if (*s == (char) c)
			return ((char *) s);
		s++;
	}
	if (!c)
		return ((char *) s);
	return (NULL);
}
